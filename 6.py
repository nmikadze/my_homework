########################################################################################################################################

def calculator():
    try:
        # შეყვანა
        input_str = input("შეიყვანეთ გამოსახულება: ")

        # კალკულაცია
        result = eval(input_str)

        # შედეგის დაბეჭდვა
        print(f"შედეგი: {result}")
    except ZeroDivisionError:
        print("გაყოფა ნულზე არ შეიძლება.")
    except Exception as e:
        print(f"შეცდომა: {e}")

# კალკულატორის ფუნ
calculator()


