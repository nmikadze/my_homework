#2. შექმენი ფუნქცია, რომელიც მიიღებს მომხმარებლის წონას და დააბრუნებს მის წონას მთვარეზე. (მთვარის გრავიტაცია 6_ჯერ ნაკლებია დედამიწისაზე
########################################################################################################################################

def print_weight_divided_by_six():
    try:
        # მომხმარებლის წონის შეტანა
        user_weight = float(input("შეიყვანეთ თქვენი წონა: "))

        # წონის გაყოფა 6-ზე და დაბეჭდვა
        result = user_weight / 6
        print(f"წონა გაყოფილი 6-ზე: {result}")
    except ValueError:
        print("შემოიტანეთ სწორი რიცხვი თქვენი წონისთვის.")

# ფუნქციის გამოძახება
print_weight_divided_by_six()




